const DEFAULT_MEANING = "Глубокая философская концепция, которая требует личного осмысления. Каждый шаг по этому пути приоткрывает новые грани вашей архитектуры.";

export const AssociativeCloud = ({ keywords }: { keywords: string[] }) => {
  const [selectedWord, setSelectedWord] = useState<string | null>(null);

  return (
    <div className="w-full mt-6">
      <h4 className="font-sans text-[0.65rem] tracking-[0.15em] uppercase text-[var(--color-muted)] mb-4">
        Ассоциативное облако
      </h4>
      
      <div className="flex flex-wrap gap-2 mb-4">
        {keywords.map((word) => {
          const isSelected = selectedWord === word;
          return (
            <button
              key={word}
              onClick={() => setSelectedWord(isSelected ? null : word)}
              className={`px-3 py-1.5 text-xs font-sans tracking-[0.05em] transition-all duration-300 rounded-[2px] border ${
                isSelected 
                  ? 'bg-[var(--color-antique-gold)] text-white border-[var(--color-antique-gold)] shadow-md' 
                  : 'bg-transparent text-[var(--color-graphite)] border-[var(--color-antique-gold)] border-opacity-30 hover:border-opacity-100 hover:bg-[var(--color-ivory)]'
              }`}
            >
              {word}
            </button>
          );
        })}
      </div>

      <AnimatePresence mode="wait">
        {selectedWord && (
          <motion.div
            key={selectedWord}
            initial={{ opacity: 0, y: 5 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: 5 }}
            transition={{ duration: 0.3 }}
            className="p-4 bg-[var(--color-ivory)] bg-marble border-l-2 border-[var(--color-antique-gold)] rounded-r-sm shadow-sm"
          >
            <h5 className="font-serif text-sm text-[var(--color-antique-gold)] mb-1 uppercase tracking-wider">
              {selectedWord}
            </h5>
            <p className="font-serif text-sm leading-relaxed text-[var(--color-graphite)] italic">
              «{PHILOSOPHICAL_DICTIONARY[selectedWord] || DEFAULT_MEANING}»
            </p>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  );
};
